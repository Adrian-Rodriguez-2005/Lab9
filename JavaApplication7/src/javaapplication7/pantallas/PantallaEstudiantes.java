    void actualizarEstudiante(Estudiante estudiante) {
       
        for (int i = 0; i < JavaApplication7.estudiantesGlobal.size(); i++) {
            Estudiante es= JavaApplication7.estudiantesGlobal.get(i);
            if(es.hashCode()==estudiante.hashCode()){
                JavaApplication7.estudiantesGlobal.set(i,estudiante);
            }
        }
        actualizarLista();
    }

    void agregarEstudiante(Estudiante estudiante) {
     JavaApplication7.estudiantesGlobal.add(estudiante);
     actualizarLista();
    }
    
    void actualizarLista(){
        modelo.clear();
        for(Estudiante es: JavaApplication7.estudiantesGlobal){
            modelo.addElement(es.getTexto());
        }
    }
